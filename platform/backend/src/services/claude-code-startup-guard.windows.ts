/**
 * PowerShell counterpart of the Claude Code startup guard renderer in
 * claude-code-startup-guard.ts — same behavior contract, expressed for
 * Windows PowerShell 5.1 and PowerShell 7+:
 *
 * - each remote is checked two ways: transport reachability AND, for the
 *   gateway/proxy, existence via the public connection-health endpoint
 *   (reachability alone cannot catch a remote deleted on the platform — the
 *   data-plane answers uniformly without auth);
 * - every row is on screen from the start (probing row bright, pending rows
 *   dim below it, text aligned into the glyph column); checks show ~0.75s
 *   of appended trailing dots (append-only output cannot
 *   flicker), all on the alternate screen so the terminal is clean after
 *   claude exits; an unreachable platform gets the 15s capped-backoff retry
 *   ladder with jitter, status line at 3s, "hang tight" at 10s, and the
 *   disconnect (Y/n) offer on its own line below the row, `y`/`n` live
 *   throughout; after the whole turn, ONE "Disconnect … from Claude now?
 *   (Y/n)" prompt covers every down remote;
 * - disconnect = the exact reverse of connect (mcp remove, settings.json
 *   key strip with a one-time backup, marketplace remove), recorded in a
 *   skip file so later launches don't re-check the remote; once nothing
 *   connected is left to check, the guard uninstalls itself entirely
 *   (script, skip file, profile wrapper blocks);
 * - never blocks a launch: non-interactive runs (redirected console, -p)
 *   only warn on stderr, and the profile wrapper always runs the real
 *   claude afterward. ARCHESTRA_CLAUDE_GUARD=0 disables it.
 *
 * The guard is installed at ~/{@link CLAUDE_CODE_GUARD_PS_SCRIPT_RELPATH} and
 * hooked via a marker-delimited `claude` function in profile.ps1 of every
 * PowerShell edition present (Windows PowerShell and PowerShell 7 keep
 * separate profile directories).
 */

/**
 * The standalone guard script body (the .ps1 file).
 *
 * @public — consumed by the install section below and exercised directly by
 * the unit tests, which knip --production ignores.
 */
export function renderClaudeCodeStartupGuardPowerShell(
  ctx: ClaudeCodeStartupGuardContext,
): string {
  const resources = guardResources(ctx);
  const remoteEntries = resources
    .map(
      (r) =>
        `  @{ Label = ${psq(r.label)}; Url = ${psq(r.url)}; Kind = ${psq(r.kind)}; FailName = ${psq(r.failName)}; DownMarker = ${psq(r.downMarker ?? "")} }`,
    )
    .join("\n");

  return `# ${ctx.appName} pre-loader for Claude Code (PowerShell) — generated by the ${ctx.appName} /connection page.
# Checks the ${ctx.appName} remotes wired into Claude Code before it starts —
# one platform health request for all of them — and offers to disconnect
# everything that is down in one keypress (the reverse of connect). It never
# blocks the launch: the profile wrapper runs the real claude no matter how
# this script exits. Disable with ARCHESTRA_CLAUDE_GUARD=0.

if ($env:ARCHESTRA_CLAUDE_GUARD -eq '0') { exit 0 }
$ErrorActionPreference = 'Continue'
# Invoke-WebRequest paints its progress banner across the TOP console rows —
# straight over the logo — on every request unless progress is silenced.
$ProgressPreference = 'SilentlyContinue'

$AppName = ${psq(ctx.appName)}
# One request answers for every checkable remote ('' = no health endpoint
# derivable; the guard then falls back to per-resource reachability probes).
# A response without a down marker (an older backend 404ing the route, a 429)
# reads as ok — version skew and rate limiting can never look like an outage.
$HealthUrl = ${psq(ctx.healthUrl ?? "")}
$Remotes = @(
${remoteEntries}
)${
    ctx.mcp
      ? `
$McpServerName = ${psq(ctx.mcp.serverName)}`
      : ""
  }${
    ctx.skills
      ? `
$SkillsMarketplaceName = ${psq(ctx.skills.marketplaceName)}`
      : ""
  }

$GuardPath = Join-Path $env:USERPROFILE ${psq(CLAUDE_CODE_GUARD_PS_SCRIPT_RELPATH)}
# Remotes this guard's own disconnect action already removed from Claude
# Code, one kind per line. They are skipped below; connect clears the file.
$SkipFile = Join-Path $env:USERPROFILE ${psq(CLAUDE_CODE_GUARD_SKIP_RELPATH)}
$DisconnectedKinds = @()
if (Test-Path $SkipFile) { $DisconnectedKinds = @(Get-Content -Path $SkipFile | Where-Object { $_ }) }
$ActiveRemotes = @($Remotes | Where-Object { $DisconnectedKinds -notcontains $_.Kind })

function Add-ArchDisconnected([string]$Kind) { try { Add-Content -Path $SkipFile -Value $Kind } catch { } }

# Once nothing connected is left to check, the guard removes itself entirely
# — script, skip file, and the profile wrapper blocks. A leftover no-op hook
# is a dependency that can only ever break a future claude launch (deleted
# files, reconfigured shells); connect re-installs everything.
function Remove-ArchGuard {
  Remove-Item -Force -ErrorAction SilentlyContinue $GuardPath, $SkipFile
  $profilePaths = @()
  $docs = [Environment]::GetFolderPath('MyDocuments')
  if ($docs) {
    foreach ($edition in @('WindowsPowerShell', 'PowerShell')) {
      $profilePaths += (Join-Path (Join-Path $docs $edition) 'profile.ps1')
    }
  }
  try { if ($PROFILE -and $PROFILE.CurrentUserAllHosts) { $profilePaths += [string]$PROFILE.CurrentUserAllHosts } } catch { }
  foreach ($profilePath in ($profilePaths | Select-Object -Unique)) {
    if (-not (Test-Path $profilePath)) { continue }
    $kept = @()
    $skip = $false
    foreach ($line in @(Get-Content -Path $profilePath)) {
      if ($line -eq ${psq(CLAUDE_CODE_GUARD_MARKER_START)}) { $skip = $true; continue }
      if ($line -eq ${psq(CLAUDE_CODE_GUARD_MARKER_END)}) { $skip = $false; continue }
      if (-not $skip) { $kept += $line }
    }
    try { Set-Content -Path $profilePath -Value ($kept -join [Environment]::NewLine) } catch { }
  }
}

if ($ActiveRemotes.Count -eq 0) { Remove-ArchGuard; exit 0 }

# Retry budget for the single health request when the platform is
# unreachable: capped exponential backoff (1,2,4,4...s) + 0-1s jitter, 15s
# total. The status line appears after 3s, "hang tight" after 10s. When the
# budget runs out every remote is treated as down.
$RetryTotalSeconds = 15
$NoticeAfterSeconds = 3
$HangTightAfterSeconds = 10

# Each check shows ~0.75s of animation — enough to read as a deliberate
# step, short enough to never feel like waiting. A tick appends one
# unhurried trailing dot every ~250ms.
$MinCheckFrames = 3
$FrameSleepMs = 250

# Only drive the terminal (and prompt) when a human is watching: a real
# console on both ends and no -p/--print run. Otherwise probe once per remote,
# warn on stderr, and get out of the way — automation must never wait on us.
$Interactive = $true
foreach ($a in $args) { if ($a -eq '-p' -or $a -eq '--print') { $Interactive = $false } }
try {
  if ([Console]::IsInputRedirected -or [Console]::IsOutputRedirected) { $Interactive = $false }
} catch { $Interactive = $false }

$UseColor = [string]::IsNullOrEmpty($env:NO_COLOR)
$Esc = [char]27
$UseVt = $false
try { $UseVt = [bool]$Host.UI.SupportsVirtualTerminal } catch { }
# ANSI color codes, matching the console palette's bright colors. Emitted as
# raw VT when the host supports it: Windows PowerShell 5.1's console-API
# colors (-ForegroundColor) stop rendering on the alternate screen buffer
# under conpty, while VT sequences always work there.
$VtCodes = @{ Cyan = '96'; Magenta = '95'; Red = '91'; Yellow = '93'; DarkGray = '90'; White = '97' }

function Write-Arch([string]$Text, [string]$Color, [switch]$NoNewline) {
  if ($UseColor -and $Color -and $UseVt) {
    Write-Host -NoNewline:$NoNewline ("$Esc[" + $VtCodes[$Color] + 'm' + $Text + "$Esc[0m")
  } elseif ($UseColor -and $Color) {
    Write-Host -NoNewline:$NoNewline -ForegroundColor $Color $Text
  } else {
    Write-Host -NoNewline:$NoNewline $Text
  }
}

# One attempt at the single health request. Returns $true when the platform
# answered (any HTTP response counts — on 5.1 WebException and on 7+
# HttpResponseException both expose .Response only when a server answered);
# the body lands in $Script:HealthBody for per-resource markers.
$Script:HealthBody = ''
$Script:HealthState = ''  # 'ok' | 'down' | '' (no health URL)
function Invoke-ArchHealthFetch {
  try {
    $resp = Invoke-WebRequest -Uri $HealthUrl -Method Get -TimeoutSec 3 -UseBasicParsing -ErrorAction Stop
    # normalize whitespace so the down markers match regardless of how the
    # JSON is formatted (a pretty-printing proxy must not fail-open silently)
    $Script:HealthBody = ([string]$resp.Content) -replace 's', ''
    return $true
  } catch {
    if ($_.Exception.PSObject.Properties['Response'] -and $_.Exception.Response) {
      $Script:HealthBody = ''
      return $true
    }
    return $false
  }
}

# Reachability-only probe, used when no health URL could be derived.
function Test-ArchReachable([string]$Url) {
  try {
    $null = Invoke-WebRequest -Uri $Url -Method Get -TimeoutSec 3 -UseBasicParsing -ErrorAction Stop
    return $true
  } catch {
    return [bool]($_.Exception.PSObject.Properties['Response'] -and $_.Exception.Response)
  }
}

function Test-ArchResourceDown($r) {
  if (-not $HealthUrl) { return -not (Test-ArchReachable $r.Url) }
  if ($Script:HealthState -eq 'down') { return $true }
  if (-not $r.DownMarker) { return $false }
  return $Script:HealthBody.Contains($r.DownMarker)
}

if (-not $Interactive) {
  if ($HealthUrl) {
    if (-not (Invoke-ArchHealthFetch)) { $Script:HealthState = 'down' }
  }
  foreach ($r in $ActiveRemotes) {
    if (Test-ArchResourceDown $r) {
      [Console]::Error.WriteLine('archestra: failed to connect to ' + $r.FailName + ' — claude is configured to use it and may fail. Disconnect it from the ' + $AppName + ' /connection page, or run claude interactively to be offered a disconnect.')
    }
  }
  exit 0
}

function Clear-ArchLine {
  $w = 80; try { $w = [Console]::WindowWidth } catch { }
  Write-Host -NoNewline ("\`r" + (' ' * [Math]::Max($w - 1, 1)) + "\`r")
}

# Progress is the row's text growing dim trailing dots — each tick only
# APPENDS one character, never rewrites the line, so nothing can flicker
# by construction. (Glyph spinners redraw in place every frame; every
# terminal renders that as some degree of strobing — caught live on
# Windows Terminal.) The line wraps back after a few dots so a slow
# disconnect can't grow it forever. The probing row prints bright — dim is
# reserved for the pending rows waiting below it — and its two leading
# spaces reserve the glyph column, so the first text character lines up
# across pending, probing, and probed rows.
$Script:SpinText = ''
$Script:SpinDots = 0
function Show-ArchSpinStart([string]$Label, [string]$Suffix) {
  $text = $Label
  if ($Suffix) { $text = $Label + ' ' + $Suffix }
  $Script:SpinText = $text
  $Script:SpinDots = 0
  Clear-ArchLine
  Write-Host -NoNewline ('  ' + $text)
}
function Show-ArchSpinTick {
  $Script:SpinDots++
  if ($Script:SpinDots -gt 8) {
    Show-ArchSpinStart $Script:SpinText ''
    return
  }
  Write-Arch '.' DarkGray -NoNewline
}

function Read-ArchKey { # non-blocking; '' when no key is waiting
  try {
    if ([Console]::KeyAvailable) { return [string][Console]::ReadKey($true).KeyChar }
  } catch { }
  return ''
}

function Show-ArchOk([string]$Label) {
  Clear-ArchLine
  Write-Arch '✓' Magenta -NoNewline
  Write-Host (' ' + $Label)
}

# Status glyphs stay in the narrow ranges (○ ✓ ✗) so every row's icon and
# text start in the same column — the heavy ✖/✔ render double-width in
# common Windows fonts and break the alignment.
function Show-ArchDown($r) {
  Clear-ArchLine
  Write-Arch ('✗ Failed to connect to ' + $r.FailName) Red
}

function Get-ArchClaudeExe {
  # The profile wraps \`claude\` in a function, so resolve the real executable.
  Get-Command -Name claude -CommandType Application -ErrorAction SilentlyContinue | Select-Object -First 1
}

# The reverse-of-connect commands for one remote, silenced. Shared by the
# down-prompt disconnect and the reconfigure menu so the two can't drift.
function Invoke-ArchDisconnectActions([string]$Kind) {
  $claudeExe = Get-ArchClaudeExe
  switch ($Kind) {${
    ctx.mcp
      ? `
    'mcp' {
      if ($claudeExe) {
        try { & $claudeExe.Source mcp remove --scope user $McpServerName 2>$null | Out-Null } catch { }
        try { & $claudeExe.Source mcp remove --scope local $McpServerName 2>$null | Out-Null } catch { }
      }
    }`
      : ""
  }${
    ctx.skills
      ? `
    'skills' {
      if ($claudeExe) {
        try { & $claudeExe.Source plugin marketplace remove $SkillsMarketplaceName 2>$null | Out-Null } catch { }
      }
    }`
      : ""
  }${
    ctx.proxy
      ? `
    'proxy' { Disconnect-ArchProxy }`
      : ""
  }
  }
}

# Reversing a connect step animates the same way the probes do: the dots
# grow around the commands, and the row lands on a check.
function Disconnect-ArchRemote([string]$Kind, [string]$Label) {
  Show-ArchSpinStart ('Disconnecting ' + $Label) ''
  for ($pad = 0; $pad -lt 2; $pad++) { Start-Sleep -Milliseconds $FrameSleepMs; Show-ArchSpinTick }
  Invoke-ArchDisconnectActions $Kind
  for ($pad = 0; $pad -lt 2; $pad++) { Start-Sleep -Milliseconds $FrameSleepMs; Show-ArchSpinTick }
  Clear-ArchLine
  Write-Arch '✓' Magenta -NoNewline
  Write-Host (' Disconnected ' + $Label)${
    ctx.proxy?.provider === "bedrock"
      ? `
  if ($Kind -eq 'proxy') {
    Clear-ArchLine
    Write-Arch '  If you set AWS_BEARER_TOKEN_BEDROCK in your environment, remove it there too.' DarkGray
  }`
      : ""
  }
}

${ctx.proxy ? disconnectProxyFunction(ctx) : ""}

function Disconnect-ArchRemotes($remotes) { # reverse connect, then skip on later launches
  foreach ($r in $remotes) {
    Disconnect-ArchRemote $r.Kind $r.Label
    Add-ArchDisconnected $r.Kind
  }
}

# ---- Reconfigure menu -------------------------------------------------
# Opened with [C] from the prompt under the rows. Every remote is already on
# screen in a stable block, so the menu just re-decorates those rows in place
# ([n] label) and reads number keys — no redraw, no layout jump. Pressing a
# number disconnects that remote (reverse-of-connect, then remembered) and
# lands its row on the purple check; Esc or Enter leaves and lets claude
# start. Removing the last connected remote takes the guard with it. Rows
# list every active remote regardless of reachability. VT uses relative
# cursor moves off the block; legacy consoles use absolute positioning.
function Move-ArchRowStart([int]$i, [int]$count, [int]$baseTop) {
  if ($UseVt) { Write-Host -NoNewline ("$Esc[" + ($count - $i) + "A\`r$Esc[2K") }
  else { try { [Console]::SetCursorPosition(0, $baseTop - $count + $i) } catch { }; Clear-ArchLine }
}
function Move-ArchRowEnd([int]$i, [int]$count, [int]$baseTop) {
  if ($UseVt) { Write-Host -NoNewline ("$Esc[" + ($count - $i) + "B\`r") }
  else { try { [Console]::SetCursorPosition(0, $baseTop) } catch { } }
}
function Show-ArchMenuRow([int]$i, [int]$count, [int]$baseTop, $done) {
  $r = $ActiveRemotes[$i]
  Move-ArchRowStart $i $count $baseTop
  if ($done.ContainsKey($r.Kind)) {
    Write-Arch '✓' Magenta -NoNewline
    Write-Host -NoNewline (' Disconnected ' + $r.Label)
  } else {
    Write-Arch ('[' + ($i + 1) + ']') Magenta -NoNewline
    Write-Host -NoNewline (' ' + $r.Label)
  }
  Move-ArchRowEnd $i $count $baseTop
}
function Disconnect-ArchMenuRow([int]$i, [int]$count, [int]$baseTop) {
  $r = $ActiveRemotes[$i]
  Move-ArchRowStart $i $count $baseTop
  Show-ArchSpinStart ('Disconnecting ' + $r.Label) ''
  for ($p = 0; $p -lt 2; $p++) { Start-Sleep -Milliseconds $FrameSleepMs; Show-ArchSpinTick }
  Invoke-ArchDisconnectActions $r.Kind
  for ($p = 0; $p -lt 2; $p++) { Start-Sleep -Milliseconds $FrameSleepMs; Show-ArchSpinTick }
  Clear-ArchLine
  Write-Arch '✓' Magenta -NoNewline
  Write-Host -NoNewline (' Disconnected ' + $r.Label)
  Move-ArchRowEnd $i $count $baseTop
  Add-ArchDisconnected $r.Kind
}
function Show-ArchMenuRowResult([int]$i, [int]$count, [int]$baseTop) {
  $r = $ActiveRemotes[$i]
  Move-ArchRowStart $i $count $baseTop
  if (Test-ArchResourceDown $r) { Write-Arch ('✗ Failed to connect to ' + $r.FailName) Red -NoNewline }
  else { Write-Arch '✓' Magenta -NoNewline; Write-Host -NoNewline (' ' + $r.Label) }
  Move-ArchRowEnd $i $count $baseTop
}
function Show-ArchMenuFooter([int]$count, [int]$baseTop) {
  $text = '  Press 1-' + $count + ' to disconnect a resource from Claude · [Esc] Done'
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K")
    Write-Arch $text DarkGray -NoNewline
    Write-Host -NoNewline ("$Esc" + '8')
  } else {
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }
    Clear-ArchLine; Write-Arch $text DarkGray -NoNewline
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
}
function Clear-ArchMenuFooter([int]$baseTop) {
  if ($UseVt) { Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K$Esc" + '8') }
  else {
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }
    Clear-ArchLine
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
}
function Invoke-ArchReconfigureMenu {
  $Script:Dwell = $true
  $count = $ActiveRemotes.Count
  $baseTop = 0; try { $baseTop = [Console]::CursorTop } catch { }
  $done = @{}
  for ($i = 0; $i -lt $count; $i++) { Show-ArchMenuRow $i $count $baseTop $done }
  Show-ArchMenuFooter $count $baseTop
  while ($true) {
    $k = $null
    try { $k = [Console]::ReadKey($true) } catch { break }
    if ($k.Key -eq 'Enter' -or $k.Key -eq 'Escape' -or $k.KeyChar -eq 'q' -or $k.KeyChar -eq 'Q') { break }
    $d = 0
    if ([int]::TryParse([string]$k.KeyChar, [ref]$d) -and $d -ge 1 -and $d -le $count) {
      $r = $ActiveRemotes[$d - 1]
      if ($done.ContainsKey($r.Kind)) { continue }
      Disconnect-ArchMenuRow ($d - 1) $count $baseTop
      $done[$r.Kind] = $true
      if ($done.Count -ge $count) { Remove-ArchGuard; break }
    }
  }
  Clear-ArchMenuFooter $baseTop
  for ($i = 0; $i -lt $count; $i++) {
    if ($done.ContainsKey($ActiveRemotes[$i].Kind)) { continue }
    Show-ArchMenuRowResult $i $count $baseTop
  }
}

# The persistent [C] entry. On VT it is drawn once BEFORE the first probe
# (cursor at the block's base) and stays on screen the whole run; legacy
# consoles draw it in the closing tail instead. One line below the block.
function Show-ArchReconfigureHint {
  $text = '  To reconfigure your ' + $AppName + ' connection press [C]'
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K")
    Write-Arch $text DarkGray -NoNewline
    Write-Host -NoNewline ("$Esc" + '8')
  } else {
    $baseTop = 0; try { $baseTop = [Console]::CursorTop } catch { }
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }
    Clear-ArchLine; Write-Arch $text DarkGray -NoNewline
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
}
function Clear-ArchReconfigureHint {
  if ($UseVt) { Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K$Esc" + '8') }
  else {
    $baseTop = 0; try { $baseTop = [Console]::CursorTop } catch { }
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }
    Clear-ArchLine
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
}

# The healthy pass's closing beat: on legacy consoles draw the hint now; on VT
# it is already on screen. Either way wait ~1.5s for [C] (a press from during
# the probes is buffered and read here), then clear and open the menu on 'c'.
function Show-ArchReconfigureOffer {
  if (-not $UseVt) { Show-ArchReconfigureHint }
  $key = ''
  $deadline = [DateTime]::UtcNow.AddMilliseconds(1500)
  while ([DateTime]::UtcNow -lt $deadline) {
    $key = Read-ArchKey
    if ($key) { break }
    Start-Sleep -Milliseconds 40
  }
  Clear-ArchReconfigureHint
  if ($key -eq 'c' -or $key -eq 'C') { Invoke-ArchReconfigureMenu }
}

# Every down remote already got its failure line during the turn; this single
# prompt then covers them all — disconnect everything that failed in one
# keypress, or skip them all and go straight to claude. When every remote is
# down and the user disconnects, nothing is left to check, so the guard
# silently removes itself too — the Disconnected rows say everything.
function Show-ArchDownSummaryPrompt($downRemotes) {
  $baseTop = 0; try { $baseTop = [Console]::CursorTop } catch { }
  if ($downRemotes.Count -eq 1) {
    $q = 'Disconnect ' + $downRemotes[0].FailName + ' from Claude now? (Y/n) '
  } else {
    $q = 'Disconnect all ' + $downRemotes.Count + ' unreachable resources from Claude now? (Y/n) '
  }
  $hint = '  or press [C] to reconfigure your ' + $AppName + ' connection'
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K" + $q + "$Esc[1B\`r$Esc[2K")
    Write-Arch $hint DarkGray -NoNewline
    Write-Host -NoNewline ("$Esc" + '8')
  } else {
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }
    Clear-ArchLine; Write-Host -NoNewline $q
    try { [Console]::SetCursorPosition(0, $baseTop + 2) } catch { }
    Clear-ArchLine; Write-Arch $hint DarkGray -NoNewline
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
  $choice = 'n'
  try {
    $k = [Console]::ReadKey($true)
    if ($k.Key -eq 'Enter' -or $k.KeyChar -eq 'y' -or $k.KeyChar -eq 'Y') { $choice = 'y' }
    elseif ($k.KeyChar -eq 'c' -or $k.KeyChar -eq 'C') { $choice = 'c' }
  } catch { }
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[1B\`r$Esc[2K$Esc[1B\`r$Esc[2K$Esc" + '8')
  } else {
    try { [Console]::SetCursorPosition(0, $baseTop + 1) } catch { }; Clear-ArchLine
    try { [Console]::SetCursorPosition(0, $baseTop + 2) } catch { }; Clear-ArchLine
    try { [Console]::SetCursorPosition(0, $baseTop) } catch { }
  }
  if ($choice -eq 'c') { Invoke-ArchReconfigureMenu; return }
  if ($choice -eq 'y') {
    $Script:Dwell = $true
    Disconnect-ArchRemotes $downRemotes
    if ($downRemotes.Count -ge $ActiveRemotes.Count) { Remove-ArchGuard }
    return
  }
  Clear-ArchLine
  Write-Arch '○' Yellow -NoNewline
  if ($downRemotes.Count -eq 1) { Write-Arch ' Skipped — still configured; Claude may fail to reach it this session' DarkGray }
  else { Write-Arch ' Skipped — still configured; Claude may fail to reach them this session' DarkGray }
}

# The disconnect offer during the retry ladder is the same classic (Y/n)
# prompt, on its own line below the row after a blank line — drawn once via
# cursor save/restore so the dots keep appending to the row above it, and
# wiped again the moment the wait resolves. The platform being unreachable
# affects every active remote, so answering yes disconnects them all.
$Script:WaitPromptShown = $false
function Get-ArchWaitPromptText {
  if ($ActiveRemotes.Count -eq 1) { return 'Disconnect ' + $ActiveRemotes[0].FailName + ' from Claude now? (Y/n) ' }
  return 'Disconnect all ' + $ActiveRemotes.Count + ' unreachable resources from Claude now? (Y/n) '
}
function Show-ArchWaitPrompt {
  if ($Script:WaitPromptShown) { return }
  $Script:WaitPromptShown = $true
  # one blank line below the whole row block (on VT the pending rows are
  # already on screen beneath the probing one; without VT rows still draw
  # one at a time, so the prompt sits two lines below the row)
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[" + ($ActiveRemotes.Count + 2) + 'B' + "\`r" + "$Esc[2K" + (Get-ArchWaitPromptText) + "$Esc" + '8')
  } else {
    try {
      $x = [Console]::CursorLeft; $y = [Console]::CursorTop
      Write-Host ''
      Write-Host ''
      Write-Host -NoNewline (Get-ArchWaitPromptText)
      [Console]::SetCursorPosition($x, $y)
    } catch { }
  }
}
function Clear-ArchWaitPrompt {
  if (-not $Script:WaitPromptShown) { return }
  $Script:WaitPromptShown = $false
  if ($UseVt) {
    Write-Host -NoNewline ("$Esc" + '7' + "$Esc[" + ($ActiveRemotes.Count + 2) + 'B' + "\`r" + "$Esc[2K" + "$Esc" + '8')
  } else {
    try {
      $x = [Console]::CursorLeft; $y = [Console]::CursorTop
      $w = 80; try { $w = [Console]::WindowWidth } catch { }
      [Console]::SetCursorPosition(0, $y + 2)
      Write-Host -NoNewline (' ' * [Math]::Max($w - 1, 1))
      [Console]::SetCursorPosition($x, $y)
    } catch { }
  }
}

# One health request for the whole launch, retried with backoff while the
# spinner plays on the first resource row. y = disconnect every remote now
# (they are all unreachable) — and with nothing left to check, remove the
# startup check itself; n = skip the checks for this launch. Both answer
# keys are live the whole wait; bare Enter accepts the disconnect default
# only once the prompt is actually on screen.
$Script:SkipAll = $false
$Script:DiscAll = $false
$Script:OpenMenu = $false
$Script:LastWaitNote = ''
function Wait-ArchHealth {
  if (Invoke-ArchHealthFetch) { $Script:HealthState = 'ok'; return }
  $start = [DateTime]::UtcNow
  $delay = 1
  $nextAttempt = $start.AddSeconds(1)
  $lastElapsed = 0
  while ($true) {
    Start-Sleep -Milliseconds 250
    $key = Read-ArchKey
    if ($key -eq 'y' -or $key -eq 'Y') { $Script:DiscAll = $true; break }
    if ($key -eq 'n' -or $key -eq 'N') { $Script:SkipAll = $true; break }
    if ($key -eq 'c' -or $key -eq 'C') { $Script:OpenMenu = $true; break }
    if ($key -eq "\`r" -and $Script:WaitPromptShown) { $Script:DiscAll = $true; break }
    $now = [DateTime]::UtcNow
    if ($now -ge $nextAttempt) {
      if (Invoke-ArchHealthFetch) { $Script:HealthState = 'ok'; Clear-ArchWaitPrompt; return }
      $delay = [Math]::Min($delay * 2, 4)
      $nextAttempt = $now.AddSeconds($delay + (Get-Random -Minimum 0 -Maximum 2))
    }
    $elapsed = [int]([DateTime]::UtcNow - $start).TotalSeconds
    # the wall clock can step backwards mid-wait (NTP); the counter on
    # screen must never run back
    if ($elapsed -lt $lastElapsed) { $elapsed = $lastElapsed }
    $lastElapsed = $elapsed
    if ($elapsed -ge $RetryTotalSeconds) { break }
    $waitNote = ''
    if ($elapsed -ge $HangTightAfterSeconds) {
      $waitNote = '— trying to connect... ' + $elapsed + 's, few more seconds, hang tight...'
    } elseif ($elapsed -ge $NoticeAfterSeconds) {
      $waitNote = '— trying to connect... ' + $elapsed + 's'
    }
    # redraw the full line only when its text changed; otherwise just the
    # spinner glyph moves (see Show-ArchSpinTick)
    if ($waitNote -eq $Script:LastWaitNote) {
      Show-ArchSpinTick
    } else {
      $Script:LastWaitNote = $waitNote
      Show-ArchSpinStart $ActiveRemotes[0].Label $waitNote
      if ($waitNote) { Show-ArchWaitPrompt }
    }
  }
  Clear-ArchWaitPrompt
  $Script:HealthState = 'down'
}

# The whole pre-loader draws on the terminal's alternate screen — the same
# way claude itself does — so nothing lingers in the scrollback after claude
# exits. When the launch needed attention, the outcome is held briefly
# before the alternate screen closes over it.
$Script:AltScreen = $UseVt
$Script:Dwell = $false
function Exit-ArchGuard {
  if ($Script:Dwell) { Start-Sleep -Milliseconds 1200 }
  if ($Script:AltScreen) { Write-Host -NoNewline ("$Esc[?1049l") }
  exit 0
}
if ($Script:AltScreen) { Write-Host -NoNewline ("$Esc[?1049h$Esc[H$Esc[2J") }
else { try { Clear-Host } catch { } }
${guardHeader(ctx)}
# Every row is on screen from the start: probed rows keep their glyph, the
# probing row is bright, and everything still waiting sits dim below it.
# (Legacy consoles without VT keep the one-row-at-a-time rendering.)
if ($UseVt) {
  foreach ($r in $ActiveRemotes) { Write-Arch ('  ' + $r.Label) DarkGray }
  # the [C] entry is drawn before the first probe so it shows the whole run
  Show-ArchReconfigureHint
  Write-Host -NoNewline ("$Esc[" + $ActiveRemotes.Count + 'A')
}
if ($HealthUrl) {
  Show-ArchSpinStart $ActiveRemotes[0].Label ''
  Wait-ArchHealth
}
if ($Script:OpenMenu) {
  if ($UseVt) { Write-Host -NoNewline ("$Esc[" + $ActiveRemotes.Count + 'B' + "\`r") }
  Clear-ArchReconfigureHint
  Invoke-ArchReconfigureMenu
  Exit-ArchGuard
}
if ($Script:DiscAll) {
  Clear-ArchLine
  $Script:Dwell = $true
  Disconnect-ArchRemotes $ActiveRemotes
  Remove-ArchGuard
  Exit-ArchGuard
}
if ($Script:SkipAll) {
  Clear-ArchLine
  Write-Arch '○' Yellow -NoNewline
  Write-Arch (' Skipped — remotes stay configured; Claude may fail to reach them this session') DarkGray
  if ($UseVt) { Write-Host -NoNewline ("$Esc" + '[J') }
  Exit-ArchGuard
}
$DownRemotes = @()
foreach ($r in $ActiveRemotes) {
  Show-ArchSpinStart $r.Label ''
  for ($pad = 0; $pad -lt $MinCheckFrames; $pad++) {
    Start-Sleep -Milliseconds $FrameSleepMs
    Show-ArchSpinTick
  }
  if (Test-ArchResourceDown $r) { Show-ArchDown $r; $DownRemotes += $r }
  else { Show-ArchOk $r.Label }
}
if ($DownRemotes.Count -gt 0) {
  if ($UseVt) { Clear-ArchReconfigureHint }
  Show-ArchDownSummaryPrompt $DownRemotes
} else {
  Show-ArchReconfigureOffer
}
Exit-ArchGuard
`;
}

/**
 * The Windows setup-script section that installs the guard: writes the .ps1
 * (UTF-8 with BOM so Windows PowerShell 5.1 reads the glyphs correctly) and
 * hooks the `claude` wrapper function into profile.ps1 of every PowerShell
 * edition present, inside an idempotent marker block. Relies on the setup
 * script's Say/Ok helpers being defined.
 */
export function buildWindowsClaudeCodeStartupGuardInstallSection(
  ctx: ClaudeCodeStartupGuardContext,
): string {
  return `Say ${psq(`Installing the ${ctx.appName} startup guard for Claude Code`)}
$archGuardPath = Join-Path $env:USERPROFILE ${psq(CLAUDE_CODE_GUARD_PS_SCRIPT_RELPATH)}
$null = New-Item -ItemType Directory -Force -Path (Split-Path -Parent $archGuardPath)
$archGuardBody = @'
${renderClaudeCodeStartupGuardPowerShell(ctx)}'@
[IO.File]::WriteAllText($archGuardPath, $archGuardBody, (New-Object System.Text.UTF8Encoding $true))
Write-Host ('Updated ' + $archGuardPath)
# A fresh connect re-arms every check: forget remotes a previous guard
# disconnected.
Remove-Item -Force -ErrorAction SilentlyContinue (Join-Path $env:USERPROFILE ${psq(CLAUDE_CODE_GUARD_SKIP_RELPATH)})

# Wrap \`claude\` in each PowerShell edition's all-hosts profile so the guard
# runs before every launch. The marker block is stripped and re-added, so
# re-running connect never duplicates it. The block is pure ASCII, so profile
# file encoding never matters.
$archGuardBlock = @'
${CLAUDE_CODE_GUARD_MARKER_START}
# Pre-flight connectivity check for ${ctx.appName}-connected Claude Code.
# Remove this block and ~/${CLAUDE_CODE_GUARD_PS_SCRIPT_RELPATH} to uninstall.
function claude {
  $archGuard = Join-Path $env:USERPROFILE '${CLAUDE_CODE_GUARD_PS_SCRIPT_RELPATH}'
  if (Test-Path $archGuard) { try { & $archGuard @args } catch { } }
  $archReal = Get-Command -Name claude -CommandType Application -ErrorAction SilentlyContinue | Select-Object -First 1
  if ($archReal) { & $archReal.Source @args }
  else { Write-Error "claude executable not found on PATH" }
}
${CLAUDE_CODE_GUARD_MARKER_END}
'@
$archDocs = [Environment]::GetFolderPath('MyDocuments')
$archProfiles = @()
foreach ($archEdition in @('WindowsPowerShell', 'PowerShell')) {
  $archDir = Join-Path $archDocs $archEdition
  if (Test-Path $archDir) { $archProfiles += (Join-Path $archDir 'profile.ps1') }
}
if ($archProfiles.Count -eq 0) { $archProfiles = @($PROFILE.CurrentUserAllHosts) }
foreach ($archProfilePath in $archProfiles) {
  $archKept = @()
  if (Test-Path $archProfilePath) {
    $archSkip = $false
    foreach ($archLine in @(Get-Content -Path $archProfilePath)) {
      if ($archLine -eq ${psq(CLAUDE_CODE_GUARD_MARKER_START)}) { $archSkip = $true; continue }
      if ($archLine -eq ${psq(CLAUDE_CODE_GUARD_MARKER_END)}) { $archSkip = $false; continue }
      if (-not $archSkip) { $archKept += $archLine }
    }
  }
  $null = New-Item -ItemType Directory -Force -Path (Split-Path -Parent $archProfilePath)
  Set-Content -Path $archProfilePath -Value (($archKept + ($archGuardBlock -split "\`r?\`n")) -join [Environment]::NewLine)
  Write-Host ('Updated ' + $archProfilePath)
}
Ok ${psq(`Startup guard installed — new PowerShell sessions check your ${ctx.appName} remotes before claude starts.`)}`;
}

