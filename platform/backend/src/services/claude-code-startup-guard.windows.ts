/** Single-quote a value for PowerShell; safe for arbitrary content. */
function psq(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

/**
 * The pre-loader header: the Archestra mark with the title beside it, for the
 * default brand or one of its own variants (same rule as the bash guard and
 * the setup banner).
 */
function guardHeader(ctx: ClaudeCodeStartupGuardContext): string {
  if (!isDefaultBrandedAppName(ctx.appName)) {
    return `Write-Arch $AppName Cyan
Write-Host ''`;
  }
  const [m0, m1, m2, m3, m4] = ARCHESTRA_GUARD_MARK_LINES;
  return `Write-Arch ${psq(m0)} White
Write-Arch ${psq(m1)} White -NoNewline
Write-Arch ('      ' + $AppName) Cyan
Write-Arch ${psq(m2)} White -NoNewline
Write-Arch '       Secure access to your AI tools' DarkGray
Write-Arch ${psq(m3)} White
Write-Arch ${psq(m4)} White
Write-Host ''`;
}

/**
 * Same remotes, same pre-loader order, same down markers as the bash renderer
 * (both are fed by buildClaudeCodeStartupGuardContext). Skills marketplace
 * has no per-resource status — it rides on endpoint reachability, and a
 * revoked share link never blocks a claude launch.
 */
function guardResources(ctx: ClaudeCodeStartupGuardContext): Array<{
  label: string;
  url: string;
  kind: "proxy" | "mcp" | "skills";
  failName: string;
  downMarker: string | null;
}> {
  const resources: Array<{
    label: string;
    url: string;
    kind: "proxy" | "mcp" | "skills";
    failName: string;
    downMarker: string | null;
  }> = [];
  if (ctx.proxy) {
    resources.push({
      label: `LLM proxy (${ctx.proxy.providerLabel})`,
      url: ctx.proxy.url,
      kind: "proxy",
      failName: `LLM proxy (${ctx.proxy.ref ?? ctx.proxy.providerLabel})`,
      downMarker: ctx.proxy.ref ? `"llm":"down"` : null,
    });
  }
  if (ctx.mcp) {
    resources.push({
      label: `MCP gateway (${ctx.mcp.serverName})`,
      url: ctx.mcp.url,
      kind: "mcp",
      failName: `MCP gateway (${ctx.mcp.ref ?? ctx.mcp.serverName})`,
      downMarker: ctx.mcp.ref ? `"mcp":"down"` : null,
    });
  }
  if (ctx.skills) {
    resources.push({
      label: `Skills marketplace (${ctx.skills.marketplaceName})`,
      url: ctx.skills.cloneUrl,
      kind: "skills",
      failName: `Skills marketplace (${ctx.skills.marketplaceName})`,
      downMarker: null,
    });
  }
  return resources;
}

/**
 * The proxy disconnect action: strip exactly the env keys connect set (per
 * provider, from the shared {@link CLAUDE_CODE_PROXY_ENV_KEYS} list) from
 * ~/.claude/settings.json, keeping the user's own custom-header lines and
 * taking a one-time backup. Pure PowerShell — no python dependency on Windows.
 */
function disconnectProxyFunction(ctx: ClaudeCodeStartupGuardContext): string {
  const provider = ctx.proxy?.provider ?? "anthropic";
  const envKeys = CLAUDE_CODE_PROXY_ENV_KEYS[provider];
  const keysArray = envKeys.map((key) => psq(key)).join(", ");
  const oursArray = [EXTERNAL_AGENT_ID_HEADER, VIRTUAL_KEY_HEADER]
    .map((name) => psq(name.toLowerCase()))
    .join(", ");

  return `function Disconnect-ArchProxy {
  $path = Join-Path $env:USERPROFILE '.claude/settings.json'
  if (-not (Test-Path $path)) { return }
  $raw = Get-Content -Raw -Path $path
  if (-not ($raw -and $raw.Trim())) { return }
  try { $settings = $raw | ConvertFrom-Json } catch { return }
  if (-not $settings.PSObject.Properties['env']) { return }
  $backup = $path + '.archestra-guard-backup'
  if (-not (Test-Path $backup)) { Set-Content -Path $backup -Value $raw }
  $envBlock = $settings.env
  foreach ($k in @(${keysArray})) { $envBlock.PSObject.Properties.Remove($k) }
  # Drop only our header lines; the user's other custom headers survive.
  $ours = @(${oursArray})
  $existing = ''
  if ($envBlock.PSObject.Properties['${CLAUDE_CODE_CUSTOM_HEADERS_ENV_KEY}']) { $existing = [string]$envBlock.${CLAUDE_CODE_CUSTOM_HEADERS_ENV_KEY} }
  $kept = @()
  foreach ($ln in ($existing -split "\`r?\`n")) {
    if ($ln.Trim() -and ($ours -notcontains ($ln -split ':', 2)[0].Trim().ToLower())) { $kept += $ln }
  }
  if ($kept.Count -gt 0) {
    $joined = ($kept -join "\`n")
    if ($envBlock.PSObject.Properties['${CLAUDE_CODE_CUSTOM_HEADERS_ENV_KEY}']) { $envBlock.${CLAUDE_CODE_CUSTOM_HEADERS_ENV_KEY} = $joined }
  } else {
    $envBlock.PSObject.Properties.Remove('${CLAUDE_CODE_CUSTOM_HEADERS_ENV_KEY}')
  }
  if (@($envBlock.PSObject.Properties).Count -eq 0) { $settings.PSObject.Properties.Remove('env') }
  $settings | ConvertTo-Json -Depth 32 | Set-Content -Path $path -Encoding utf8
}`;
}
